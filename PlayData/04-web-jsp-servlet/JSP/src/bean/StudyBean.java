package bean;

public class StudyBean {
	private String product;
	private String company;
	private String indexIs;
	private String control;
	private String name;
	private String botMenu;
	
	
	public String getProduct() {
		return product;
	}
	public void setProduct(String product) {
		this.product = product;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getIndexIs() {
		return indexIs;
	}
	public void setIndexIs(String indexIs) {
		this.indexIs = indexIs;
	}
	public String getControl() {
		return control;
	}
	public void setControl(String control) {
		this.control = control;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBotMenu() {
		return botMenu;
	}
	public void setBotMenu(String botMenu) {
		this.botMenu = botMenu;
	}
	
	
}
